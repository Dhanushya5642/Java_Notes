public class Role {
    Integer roleID;
    String name;
    public Integer getRoleID() {
        return roleID;
    }
    public void setRoleID(Integer roleID) {
        this.roleID = roleID;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    
}
